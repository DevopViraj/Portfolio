// Add dynamic tech grid animation
const techGrid = document.querySelector('.tech-grid');
const gridSize = 20;
const cells = [];

function createGrid() {
    for (let i = 0; i < gridSize; i++) {
        for (let j = 0; j < gridSize; j++) {
            const cell = document.createElement('div');
            cell.className = 'grid-cell';
            cell.style.left = `${(j / gridSize) * 100}%`;
            cell.style.top = `${(i / gridSize) * 100}%`;
            techGrid.appendChild(cell);
            cells.push(cell);
        }
    }
}

function animateGrid() {
    cells.forEach(cell => {
        const random = Math.random();
        if (random > 0.8) {
            cell.style.backgroundColor = `rgba(37, 99, 235, ${Math.random() * 0.5})`;
            setTimeout(() => {
                cell.style.backgroundColor = 'transparent';
            }, 1000);
        }
    });
}

createGrid();
setInterval(animateGrid, 2000);

